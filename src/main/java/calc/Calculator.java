package calc;

public class Calculator {


    public int plus(int i, int j) {

        return i + j;
    }

    public int minus(int i, int j) {
        return i - j;
    }

    public int multiple(int i, int j) {
        return i * j;
    }

    public int divide(int i, int j) {
        return i / j;
    }
}
